# 1. Arithmetic Operators
print("=== Arithmetic Operators ===")
a = 10
b = 3

print(f"Addition: {a} + {b} = {a + b}")
print(f"Subtraction: {a} - {b} = {a - b}")
print(f"Multiplication: {a} * {b} = {a * b}")
print(f"Division: {a} / {b} = {a / b}")
print(f"Floor Division: {a} // {b} = {a // b}")
print(f"Modulus (Remainder): {a} % {b} = {a % b}")
print(f"Exponentiation: {a} ** {b} = {a ** b}")

# 2. Comparison Operators
print("\n=== Comparison Operators ===")
x = 5
y = 5
z = 10

print(f"x == y: {x == y}")  # Equal
print(f"x != z: {x != z}")  # Not equal
print(f"x < z: {x < z}")   # Less than
print(f"x > z: {x > z}")   # Greater than
print(f"x <= y: {x <= y}")  # Less than or equal
print(f"z >= y: {z >= y}")  # Greater than or equal

# 3. Logical Operators
print("\n=== Logical Operators ===")
p = True
q = False

print(f"p and q: {p and q}")  # AND
print(f"p or q: {p or q}")    # OR
print(f"not p: {not p}")      # NOT

# 4. Assignment Operators
print("\n=== Assignment Operators ===")
num = 10
num += 5  # num = num + 5
print(f"After += 5: {num}")

num -= 3  # num = num - 3
print(f"After -= 3: {num}")

num *= 2  # num = num * 2
print(f"After *= 2: {num}")

# Key Learning Points:
# - Arithmetic operators perform mathematical operations
# - Comparison operators return True or False
# - Logical operators combine boolean values
# - Assignment operators assign values to variables
